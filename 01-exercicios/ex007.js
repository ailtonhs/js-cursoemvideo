//Ordem de precedência

/*
primerio () = parêntese

segundo ** = potência

terceiro * / % = multiplicação, divisão, resto da divisão 

quarto + - = adição , subtração

*/


//simplificações

/*
var n = 3

n = n + 4 simplificado n += 4

n = n - 5 simplificado n -= 5

n = n * 4 simplificado n *= 5

n = n / 2 simplificado n /= 2

n = n ** 2 simplificado n **= 2

n = n % 5 simplificado n %= 2

*/

var num = 8 ;
num += 2; //10
num -= 5; //5
num *= 5; //25
num /= 2; //12.5
num **= 2 // 156.25
num %= 2// 0.25


console.log(num);

